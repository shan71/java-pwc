package com.demo.app;

public class Math {

	public Math() {
		System.out.println("Math Object Created..");
	}
	public int add(int a, int b) {
		return a + b;
	}

	public int multiply(int a, int b) {
		return a * b;
	}

	public int divide(int a, int b) {
		return a / b;
	}

	public int subtract(int a, int b) {
		return a - b;
	}
}
